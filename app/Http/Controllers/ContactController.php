     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request, ActionMailer $mailer)
    {
        //
        $data = $request->only('name', 'email', 'subject', 'body');
        $mailer->request($data);
        if($request->ajax())
        {
            Log::info('Email sent by ' . $data['email'] . ', from -> ' . $data['name']);
            return response()->json([ 'message' => 'Email sent!', 'is_success' => true  ]);
        }
        return redirect()->back();
    }

